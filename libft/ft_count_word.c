#include "minishell.h"

int	ft_count_word(char *s, char c)
{
	int	i;
	int	cpt;

	i = 0;
	cpt = 0;
	while (s && s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			cpt++;
		i++;
	}
	return (cpt);
}
